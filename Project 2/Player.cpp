#include "header.h"
#include "Player.h"

//******************************************************************************
// default constructor. sets the experience value to 0
//******************************************************************************
Player::Player()
{
    exp = 0;
}

//******************************************************************************
// assigns its argument e to the private member exp
//******************************************************************************
void Player::setExp(int e)
{
    exp = e;
}

//******************************************************************************
// returns the value in the private member exp
//******************************************************************************
int Player::getExp() const
{
    return exp;
}